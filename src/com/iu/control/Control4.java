package com.iu.control;

public class Control4 {

	public static void main(String[] args) {
		//
		int select = 2;
		switch(select) {
		case 1:
			System.out.println("1일때 코드진행");
			break;
		default: 
			System.out.println("그외 나머지");
		case 2:
			System.out.println("2일때 코드진행");
			break;
		}

	}

}
